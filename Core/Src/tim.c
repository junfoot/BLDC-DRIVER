/* TIM3 init function */
void MX_TIM3_Init(void)
{

  /* USER CODE BEGIN TIM3_Init 0 */

  /* USER CODE END TIM3_Init 0 */

  LL_TIM_InitTypeDef TIM_InitStruct = {0};

  /* Peripheral clock enable */
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_TIM3);

  /* TIM3 interrupt Init */
  NVIC_SetPriority(TIM3_IRQn, NVIC_EncodePriority(NVIC_GetPriorityGrouping(),4, 0));
  NVIC_EnableIRQ(TIM3_IRQn);

  /* USER CODE BEGIN TIM3_Init 1 */

  /* USER CODE END TIM3_Init 1 */
  TIM_InitStruct.Prescaler = 0;
  TIM_InitStruct.CounterMode = LL_TIM_COUNTERMODE_UP;
  TIM_InitStruct.Autoreload = 34000;
  TIM_InitStruct.ClockDivision = LL_TIM_CLOCKDIVISION_DIV1;
  LL_TIM_Init(TIM3, &TIM_InitStruct);
  LL_TIM_DisableARRPreload(TIM3);
  LL_TIM_SetClockSource(TIM3, LL_TIM_CLOCKSOURCE_INTERNAL);
  LL_TIM_SetTriggerOutput(TIM3, LL_TIM_TRGO_RESET);
  LL_TIM_DisableMasterSlaveMode(TIM3);
  /* USER CODE BEGIN TIM3_Init 2 */
	
	LL_TIM_EnableIT_UPDATE(TIM3);//TIM2更新使能
	LL_TIM_EnableCounter(TIM3);//TIM2计数使能

  /* USER CODE END TIM3_Init 2 */

}

/* USER CODE BEGIN 1 */
void PWMC_switch_on_pwm(void)
{
	/* Set all duty to 50% */
	TIM1->CCR3 = (PWM_ARR>>1);
	TIM1->CCR2 = (PWM_ARR>>1);
	TIM1->CCR1 = (PWM_ARR>>1);
	
	/* wait for a new PWM period */
	LL_TIM_ClearFlag_UPDATE(TIM1);
	while ( LL_TIM_IsActiveFlag_UPDATE(TIM1) == 0u )
	{}
	LL_TIM_ClearFlag_UPDATE(TIM1);

	/* Main PWM Output Enable */
	LL_TIM_CC_EnableChannel(TIM1, TIMxCCER_MASK_CH123);
	LL_TIM_EnableAllOutputs(TIM1);
}

void PWMC_switch_off_pwm(void)
{
	/* Main PWM Output Disable */
	LL_TIM_DisableAllOutputs(TIM1);
	
	/* wait for a new PWM period */
	LL_TIM_ClearFlag_UPDATE(TIM1);
	while ( LL_TIM_IsActiveFlag_UPDATE(TIM1) == 0u )
	{}
	LL_TIM_ClearFlag_UPDATE(TIM1);
}
